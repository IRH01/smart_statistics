package com.hhly.smartdata.model.game;

public class ChannalAgent {
	private String statDate;
	private String accountNo;
	private String promotNo;
	private String channelNo;
	private String registAmount;
	private String incomeSum;
	private String startTimes;
	private String avguseTime;
	public String getStatDate() {
		return statDate;
	}
	public void setStatDate(String statDate) {
		this.statDate = statDate;
	}
	public String getAccountNo() {
		return accountNo;
	}
	public void setAccountNo(String accountNo) {
		this.accountNo = accountNo;
	}
	public String getPromotNo() {
		return promotNo;
	}
	public void setPromotNo(String promotNo) {
		this.promotNo = promotNo;
	}
	public String getChannelNo() {
		return channelNo;
	}
	public void setChannelNo(String channelNo) {
		this.channelNo = channelNo;
	}
	public String getRegistAmount() {
		return registAmount;
	}
	public void setRegistAmount(String registAmount) {
		this.registAmount = registAmount;
	}
	public String getIncomeSum() {
		return incomeSum;
	}
	public void setIncomeSum(String incomeSum) {
		this.incomeSum = incomeSum;
	}
	public String getStartTimes() {
		return startTimes;
	}
	public void setStartTimes(String startTimes) {
		this.startTimes = startTimes;
	}
	public String getAvguseTime() {
		return avguseTime;
	}
	public void setAvguseTime(String avguseTime) {
		this.avguseTime = avguseTime;
	}
}
